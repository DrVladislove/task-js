/*
Write next functions

call them with arguments a and b
and print the results via console.log

1. Multiply(a, b)

returns result of multiplying 2 numbers'

2. Divide(a, b)

returns result of division of first argument by second argument


3. Sum(a, b)

returns sum of 2 arguments


*/

//1
function multiplying(a, b) {
  var result = a * b;
  return result;
}

//2
function dividing(a, b) {
  var result = a / b;
  return result;
}

//3
function sum(a, b) {
  var result = a + b;
  return result;
}

console.log(multiplying(100, 37));
console.log(dividing(20, 5));
console.log(sum(1, 3));
